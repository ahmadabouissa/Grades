"""
برنامج تقدير الطالب
يقوم البرنامج بطلب درجة الطالب بشكل متكرر حتى يكتب المستخدم "خروج"
يعرض التقدير: ضعيف، جيد، جيد جداً، ممتاز
"""

def تقدير_الطالب(الدرجة):
    """تحويل الدرجة الرقمية إلى تقدير نصي"""
    if 0 <= الدرجة <= 59:
        return "ضعيف"
    elif 60 <= الدرجة <= 69:
        return "جيد"
    elif 70 <= الدرجة <= 84:
        return "جيد جداً"
    elif 85 <= الدرجة <= 100:
        return "ممتاز"
    else:
        return None  # درجة خارج النطاق

def تشغيل_البرنامج():
    """الحلقة الرئيسية للبرنامج"""
    print("=" * 40)
    print("مرحباً بك في برنامج تقدير الدرجات")
    print("أدخل درجة الطالب (0-100) أو اكتب 'خروج' لإنهاء البرنامج")
    print("=" * 40)
    
    while True:
        # طلب الإدخال من المستخدم
        مدخل_المستخدم = input("\nأدخل درجة الطالب: ").strip()
        
        # شرط الخروج من البرنامج
        if مدخل_المستخدم.lower() == "خروج":
            print("شكراً لاستخدام البرنامج. إلى اللقاء!")
            break
        
        # التحقق من صحة الإدخال (معالجة الأخطاء)
        try:
            # محاولة تحويل الإدخال إلى رقم صحيح
            الدرجة = int(مدخل_المستخدم)
        except ValueError:
            # في حالة إدخال نص غير رقمي وليس "خروج"
            print("خطأ: الرجاء إدخال رقم صحيح بين 0 و 100، أو كتابة 'خروج'")
            continue  # العودة لبداية الحلقة
        
        # التحقق من أن الدرجة ضمن النطاق الصحيح
        if الدرجة < 0 or الدرجة > 100:
            print("خطأ: الدرجة يجب أن تكون بين 0 و 100. حاول مرة أخرى.")
            continue
        
        # حساب التقدير
        التقدير = تقدير_الطالب(الدرجة)
        
        # عرض النتيجة
        print(f"تقدير الطالب الذي حصل على درجة {الدرجة} هو: {التقدير}")

# نقطة بدء التنفيذ
if __name__ == "__main__":
    تشغيل_البرنامج()
